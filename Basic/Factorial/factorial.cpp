class Solution{
public:
    long long int factorial(int N){
        //code here
        if (N==0) return 1;
        else if (N==1) return 1;
        else{
            long long fact=1;
            for (int i=1;i<=N;i++){
                fact *= i;
            }
            return fact;
        }
    }
};

